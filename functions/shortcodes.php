function wp_custom_social_media($content = null) {
	ob_start(); ?>
	<?php $social_network = get_field('social_network','options'); ?>
	<?php if($social_network) :?>
		<ul>
		<?php foreach($social_network as $social) :?>
			<?php $type = $social['type']; ?>
			<?php $link = $social['link']; ?>
			<?php $telephone = $social['telephone']; ?>
			<?php if($type === "whatsapp") :?>
				<li class="--<?php echo $type ?>">
					<a class="icon-<?php echo $type ?>" href="https://api.whatsapp.com/send?phone=<?php echo clear_telephone($telephone); ?>&text=<?php _e('Hola','basewordpress') ?>" target="_blank"></a>
				</li>
			<?php else : ?>
				<li class="--<?php echo $type ?>">
					<a class="icon-<?php echo $type ?>" href="<?php echo $link ?>" target="_blank"></a>
				</li>
			<?php endif; ?>
		<?php endforeach ?>
		</ul>
	<?php endif; ?>
	<?php $content = ob_get_contents();
	ob_end_clean();
	return $content;  
}
add_shortcode('social_media_list', 'wp_custom_social_media');
